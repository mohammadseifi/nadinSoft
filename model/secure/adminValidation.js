const yup = require("yup");

exports.schema = yup.object().shape({
  userName: yup
    .string()
    .required("وارد کردن نام کاربری الزامی میباشد.")
    .min(5, "نام کاربری نمیتواند کمتر از پنج کاراکتر باشد.")
    .max(20, "نام کاربری نمیتواند بیشتر از بیست کاراکتر باشد")
    .trim("وایت اسپیس نمیتواند داشته باشد."),
  password: yup
    .string()
    .required("وارد کردن پسورد الزامی میباشد.")
    .min(8, "پسورد نباید کمتر از هشت کاراکتر باشد.")
    .trim("وایت اسپیس نباید داشته باشد."),
});
