const yup = require("yup");

exports.schema = yup.object().shape({
  title: yup
    .string()
    .min(5, "عنوان نباید کمتر از پنج کاراکتر باشد!")
    .max(50, "عنوان نباید بیشتر از پنجاه کاراکتر باشد!")
    .required("وارد کردن عنوان الزامی میباشد."),
  body: yup
    .string()
    .min(5, "متن نباید کمتر از پنج کاراکتر باشد!")
    .max(500, "متن نباید بیشتر از پانصد کاراکتر باشد!")
    .required("وارد کردن عنوان الزامی میباشد."),
});
